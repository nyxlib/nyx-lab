<!--suppress CssUnusedSymbol, CssNoGenericFontName -->
<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import {ref, onMounted} from 'vue';

import A from 'aladin-lite';

/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const props = defineProps({
    target: {
        type: String,
        required: true,
    },
});

/*--------------------------------------------------------------------------------------------------------------------*/

const divEl = ref(null);

/*--------------------------------------------------------------------------------------------------------------------*/

let skymap = null;

/*--------------------------------------------------------------------------------------------------------------------*/
/* INITIALIZATION                                                                                                     */
/*--------------------------------------------------------------------------------------------------------------------*/

onMounted( () => {

    /*----------------------------------------------------------------------------------------------------------------*/

    if(divEl.value.clientHeight === 0)
    {
        divEl.value.style.height = `${3.0 * divEl.value.clientWidth / 4.0}px`;
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    A.init.then(() => {

        skymap = A.aladin(divEl.value, {
            log: false,
            fov: 60.0,
            projection: 'SIN',
            cooFrame: 'j2000',
            /**/
            target: props.target,
            /**/
            showFrame: false,
            showCooGrid: false,
            showReticle: false,
            showStatusBar: false,
            showCooLocation: false,
            showLayersControl: false,
            showFullscreenControl: false,
            showProjectionControl: false,
            /**/
            showFov: true,
            showZoomControl: true,
            /**/
            gridOptions: {
                color: '#FF0000',
                labelSize: 10,
            }
        });

        skymap.adjustFovForObject(props.target);
    });

    /*----------------------------------------------------------------------------------------------------------------*/
});

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->

    <div ref="divEl"></div>

    <!-- *********************************************************************************************************** -->

</template>

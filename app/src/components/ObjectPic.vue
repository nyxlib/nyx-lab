<!--suppress CssUnusedSymbol, CssNoGenericFontName -->
<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import {ref, onMounted} from 'vue';

import A from 'aladin-lite';

/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const props = defineProps({
    target: {
        type: String,
        required: true,
    },
});

/*--------------------------------------------------------------------------------------------------------------------*/

const mapEl = ref(null);

/*--------------------------------------------------------------------------------------------------------------------*/
/* INITIALIZATION                                                                                                     */
/*--------------------------------------------------------------------------------------------------------------------*/

onMounted( () => {

    /*----------------------------------------------------------------------------------------------------------------*/

    if(mapEl.value.clientHeight === 0)
    {
        mapEl.value.style.height = `${3.0 * mapEl.value.clientWidth / 4.0}px`;
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    A.init.then(() => {

        A.aladin(mapEl.value, {
            log: false,
            projection: 'SIN',
            cooFrame: 'j2000',
            /**/
            target: props.target,
            /**/
            showFrame: false,
            showCooGrid: false,
            showReticle: false,
            showStatusBar: false,
            showCooLocation: false,
            showLayersControl: false,
            showFullscreenControl: false,
            showProjectionControl: false,
            /**/
            showFov: true,
            showZoomControl: true,
            /**/
            gridOptions: {
                color: '#FF0000',
                labelSize: 10,
            }
        }).adjustFovForObject(props.target);
    });

    /*----------------------------------------------------------------------------------------------------------------*/
});

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->

    <div class="border-0" ref="mapEl"></div>

    <!-- *********************************************************************************************************** -->

</template>
